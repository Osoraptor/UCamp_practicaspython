# operadores aritmeticos
print(2 + 2) # Suma
print(4 - 2) # Resta
print(3 * 7) # Multiplicación
print(15 / 3) # División
print(11 % 4) # modulo el cual nos regresa el restante de la divisón en caso de que esta no sea cabal.
print(11 // 4) #division de piso donde nos regresa cuantas veces cabe el divisor en la division
print(2 ** 3) # elevar a la potencia un número

# Operadores en cadenas de texto

print('hola' + 'mundo') # concatenación
print('hola' * 3) # Repeción
print('Hola' + 'mundo' * 3) 

#Operadores de comparación

print('Hola' == 'hola') # Igual que
print('Hola' != 'hola') # Distinto que
print(3 > 11) # Mayor que
print(11 >= 10) # Mayor o igual que
print(10 <= 10) # Menor o igual que

#Operadores de existencia

print('ola' in 'Hola') # De existencia (existe en?)
print('ola' not in 'Hola') # De inexistencia (no existe en?)

#Operadores booleanos

print(True or False) # Or verifica que se cumplan solo una de las 2 condiciones
print(True and False) # And verifica que se cumplan las 2 condiciones

#Operadores de asignación

x = 1 # Operador de asiganción estándar
x += 2 # Operador de asignación suma
x *= 3 # Operador de asignación multiplicación
x %= 4 # Operador de asignación módulo
print(x)

x = 6 * 5 + 8 / 2 ** 4
print(x)










